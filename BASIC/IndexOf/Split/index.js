// For Split

var myFd = "Nabil, Emon, Nurul, Mursalin,Zahin, Jubayer, Rony, Arafat";
console.log(myFd.split("Rony"))

// For IndexOf

var myFd = "Nabil Nurul Zahin Jubayer";
console.log(myFd.indexOf("Nurul"))

// For Slice

var myFd = "Nabil Nurul Zahin Jubayer";
console.log(myFd.slice(0, 5))

// For Replace

var myFd = "Nabil, Emon, Nurul, Mursalin, Zahin, Jubayer, Rony, Arafat";
console.log(myFd.replace("Nabil", "Nurul"))

// For Concat

var myFd = "Nabil";
var myName = "Nurul";
console.log(myFd + " " + myName)

// For Repeat

var myFd = "Nabil ";
console.log(myFd.repeat(3))

// For ToLowerCase

var myFd = "Nabil NURUL ZAHIN JUBAYER";
console.log(myFd.toLowerCase())

// For ToUpperCase

var myFd = "Nabil NURUL ZAHIN JUBAYER";
console.log(myFd.toUpperCase())