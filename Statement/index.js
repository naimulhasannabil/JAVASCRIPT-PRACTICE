// Basic Break statement
for(var i = 0; i < 10; i++) {
    if(i == 5){
        break; // Exit the loop when i equals 5
    }
    console.log(i);
}

// Continue statement

for(var i = 0; i < 10; i++) {
    if(i == 5){
        continue; // Skip the current iteration when i equals 5
    }
    console.log(i);
}